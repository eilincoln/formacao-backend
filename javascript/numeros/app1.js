// Declaração de variáveis numéricas
let numeroInteiro = 10; // Número inteiro
let numeroDecimal = 3.14; // Número decimal (ponto flutuante)

// Operações básicas com números
let soma = 10 + 5; // Soma
let subtracao = 10 - 5; // Subtração
let multiplicacao = 10 * 5; // Multiplicação
let divisao = 10 / 5; // Divisão
let restoDivisao = 10 % 3; // Resto da divisão

// Operações matemáticas
let potencia = Math.pow(2, 3); // Potência: 2 elevado à 3
let raizQuadrada = Math.sqrt(16); // Raiz quadrada de 16

// Números especiais
let infinitoPositivo = Infinity; // Infinito positivo
let infinitoNegativo = -Infinity; // Infinito negativo
let naoNumero = NaN; // Não é um número (Not-a-Number)

// Métodos e propriedades de números
let valorAbsoluto = Math.abs(-5); // Valor absoluto
let arredondamento = Math.round(3.7); // Arredondamento
let maiorNumero = Math.max(10, 20, 30); // Maior número
let menorNumero = Math.min(10, 20, 30); // Menor número
let numeroAleatorio = Math.random(); // Número aleatório entre 0 e 1

// Conversão de números para strings
let numeroString = numeroInteiro.toString(); // Convertendo número inteiro para string

// Parsing de strings para números
let stringParaNumero = parseInt("10"); // Convertendo string para número inteiro
let stringParaDecimal = parseFloat("3.14"); // Convertendo string para número decimal

// Exibição de resultados
console.log("Número inteiro:", numeroInteiro);
console.log("Número decimal:", numeroDecimal);
console.log("Soma:", soma);
console.log("Subtração:", subtracao);
console.log("Multiplicação:", multiplicacao);
console.log("Divisão:", divisao);
console.log("Resto da divisão:", restoDivisao);
console.log("Potência:", potencia);
console.log("Raiz quadrada:", raizQuadrada);
console.log("Infinito positivo:", infinitoPositivo);
console.log("Infinito negativo:", infinitoNegativo);
console.log("NaN:", naoNumero);
console.log("Valor absoluto:", valorAbsoluto);
console.log("Arredondamento:", arredondamento);
console.log("Maior número:", maiorNumero);
console.log("Menor número:", menorNumero);
console.log("Número aleatório:", numeroAleatorio);
console.log("Número convertido para string:", numeroString);
console.log("String convertida para número inteiro:", stringParaNumero);
console.log("String convertida para número decimal:", stringParaDecimal);
