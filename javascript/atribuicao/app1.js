// Declaração de variáveis
let x = 5; // Atribuição simples

// Operadores de atribuição
x += 3; // Equivalente a: x = x + 3 (Operador de atribuição de adição)
console.log("x += 3:", x);

x -= 2; // Equivalente a: x = x - 2 (Operador de atribuição de subtração)
console.log("x -= 2:", x);

x *= 4; // Equivalente a: x = x * 4 (Operador de atribuição de multiplicação)
console.log("x *= 4:", x);

x /= 2; // Equivalente a: x = x / 2 (Operador de atribuição de divisão)
console.log("x /= 2:", x);

x %= 3; // Equivalente a: x = x % 3 (Operador de atribuição de módulo)
console.log("x %= 3:", x);

x **= 2; // Equivalente a: x = x ** 2 (Operador de atribuição de potência)
console.log("x **= 2:", x);

// Atribuição usando strings
let texto = "Olá, ";
texto += "Mundo!"; // Concatenação de strings usando o operador de atribuição de adição
console.log("texto:", texto);
