// Definição de uma variável
let minhaVariavel;

// Atribuindo um valor à variável
minhaVariavel = 10;

// Acessando o valor da variável
console.log("O valor da variável é:", minhaVariavel);

// Uma variável pode armazenar diferentes tipos de dados
minhaVariavel = "Olá, mundo!";
console.log("Agora a variável tem o valor:", minhaVariavel);

// Elas também podem ser utilizadas em expressões
let outraVariavel = minhaVariavel + " Como você está?";
console.log(outraVariavel);

// Variáveis podem ser reatribuídas com um novo valor
minhaVariavel = 20;
console.log("Agora o valor da variável é:", minhaVariavel);

// É possível declarar e atribuir valor simultaneamente
let novaVariavel = 50;
console.log("Valor da nova variável:", novaVariavel);
